/**
 * Helper function to get page meta
 */
function get_red_rocket_page_meta($slug) {
    global $red_rocket_pages;
    foreach ($red_rocket_pages as $page) {
        if ($page['slug'] === $slug) {
            return $page;
        }
    }
    return null;
}

/**
 * Add to functions.php to set page-specific meta titles and descriptions
 */
function red_rocket_custom_page_meta() {
    global $red_rocket_pages;
    
    if (is_page()) {
        $current_slug = get_post_field('post_name', get_post());
        $page_meta = get_red_rocket_page_meta($current_slug);
        
        if ($page_meta) {
            // Meta description
            if (!empty($page_meta['meta_description'])) {
                echo '<meta name="description" content="' . esc_attr($page_meta['meta_description']) . '">' . "\n";
            }
        }
    }
}
// add_action('wp_head', 'red_rocket_custom_page_meta', 1);

/**
 * Filter document title for custom page titles
 */
function red_rocket_custom_page_title($title) {
    global $red_rocket_pages;
    
    if (is_page()) {
        $current_slug = get_post_field('post_name', get_post());
        $page_meta = get_red_rocket_page_meta($current_slug);
        
        if ($page_meta && !empty($page_meta['meta_title'])) {
            $title['title'] = $page_meta['meta_title'];
            unset($title['site']); // Remove site name since it's in the custom title
        }
    }
    
    return $title;
}
